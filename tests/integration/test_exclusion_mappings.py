def test_exclusion_does_not_affect_link_resolution(
    test_engine: Engine,
    test_registry: WikiRegistry,
) -> None:
    """Excluded entities can still be referenced in links (for backlinks)."""
    # Even if an entity is excluded from having its own page,
    # other pages might reference it (e.g., in effects fields)

    # Create entity ref for excluded spell
    excluded_ref = EntityRef(
        entity_type=EntityType.SPELL,
        db_id="999999",
        db_name="ExcludedSpell",
        resource_name="ExcludedSpell",
    )

    # It won't be in registry
    assert test_registry.resolve_entity(excluded_ref) is None

    # But we can still create a link using the name
    from erenshor.registry.links import RegistryLinkResolver

    linker = RegistryLinkResolver(test_registry)

    # Link generation should not crash (may return fallback link)
    link = linker.ability_link("ExcludedSpell", "Excluded Spell")
    assert link is not None
    assert "ExcludedSpell" in link or "Excluded Spell" in link
