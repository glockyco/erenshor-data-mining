from erenshor.infrastructure.storage.page_storage import PageStorage
from erenshor.registry.core import WikiRegistry
from erenshor.registry.migration import (
    MappingImporter,
    MappingRules,
    RegistryBuilder,
)


@pytest.fixture
def mapping_rules_with_exclusions() -> MappingRules:
    """Create MappingRules with test exclusions."""
    rules = MappingRules()
    rules.excluded_keys.add("spell:TestExcludedSpell")
    rules.excluded_keys.add("item:TestExcludedItem")
    rules.custom_mappings["spell:TestIncludedSpell"] = "Test Spell"
    rules.custom_mappings["skill:TestSkill"] = "Test Ability"
    return rules


def test_mapping_importer_loads_exclusions(
    temp_dir: Path, test_registry: WikiRegistry
) -> None:
    """MappingImporter should load exclusion rules from mapping.json."""
    # Create test mapping file with exclusions
    mapping_file = temp_dir / "test_mapping.json"
    mapping_file.write_text(
        """{
  "metadata": {
    "schema_version": "1.0",
    "created_at": "2025-10-01T00:00:00Z",
    "updated_at": "2025-10-01T00:00:00Z",
    "total_rules": 3
  },
  "rules": {
    "spell:ExcludedSpell": {
      "wiki_page_name": null,
      "mapping_type": "exclude",
      "reason": "Test exclusion"
    },
    "spell:IncludedSpell": {
      "wiki_page_name": "Test Spell",
      "mapping_type": "custom",
      "reason": null
    },
    "item:TestItem": {
      "wiki_page_name": "Test Item",
      "mapping_type": "custom",
      "reason": null
    }
  }
}"""
    )

    importer = MappingImporter()
    rules = importer.import_manual_mappings(mapping_file, test_registry)

    # Verify exclusion is loaded
    assert rules.is_excluded("spell:ExcludedSpell")
    assert not rules.is_excluded("spell:IncludedSpell")
    assert not rules.is_excluded("item:TestItem")

    # Verify custom mappings are loaded
    assert rules.get_page_title("spell:IncludedSpell") == "Test Spell"
    assert rules.get_page_title("item:TestItem") == "Test Item"
    assert rules.get_page_title("spell:ExcludedSpell") is None


def test_excluded_entity_not_registered(
    test_engine: Engine,
    test_registry: WikiRegistry,
    mapping_rules_with_exclusions: MappingRules,
) -> None:
    """Excluded entities should not be registered in the registry."""
    builder = RegistryBuilder()
    builder.build_from_db(test_engine, test_registry, mapping_rules_with_exclusions)

    # Check excluded entities are not in registry
    excluded_spell = EntityRef(
        entity_type=EntityType.SPELL,
        db_id="999999",
        db_name="TestExcludedSpell",
        resource_name="TestExcludedSpell",
    )

    page = test_registry.resolve_entity(excluded_spell)
    assert page is None, "Excluded entity should not resolve to a page"


def test_excluded_entity_not_generated(
    test_engine: Engine,
    test_registry: WikiRegistry,
    ability_update_service: UpdateService,
    test_output_storage: PageStorage,
) -> None:
    """Generators should skip excluded entities."""
    # Note: This test assumes test database has spells/skills
    # and mapping.json excludes at least one spell

    # Register all entities except exclusions
    builder = RegistryBuilder()
    builder.build_from_db(test_engine, test_registry, None)

    # Create exclusion rule for a known spell
    # (In real scenario, this would be from mapping.json)
    from erenshor.infrastructure.database.repositories import get_spells

    spells = get_spells(test_engine, obtainable_only=False)
    if not spells:
        pytest.skip("No spells in test database")

    # Pick first spell to exclude
    excluded_spell = spells[0]
    excluded_ref = EntityRef.from_spell(excluded_spell)

    # Remove it from registry to simulate exclusion
    test_registry.by_entity.pop(excluded_ref.uid, None)

    # Run ability generator
    list(ability_update_service.update_pages(test_engine))

    # Verify excluded spell was not generated
    # Excluded spell's entity should not be in registry
    assert excluded_ref.uid not in test_registry.by_entity


def test_multi_entity_page_with_exclusion(
    test_engine: Engine,
    test_registry: WikiRegistry,
    ability_update_service: UpdateService,
    test_output_storage: PageStorage,
) -> None:
    """When one entity is excluded from a multi-entity page, only non-excluded entities should appear."""
    # This is the "Lingering Inferno" scenario:
    # - skill:Lingering Inferno → "Lingering Inferno" (included)
    # - spell:ARC - Lingering Inferno → exclude

    # For this test, we'd need a database with such a case
    # Since test database may not have it, we'll skip if not available

    # Check if Lingering Inferno exists in test database
    from erenshor.infrastructure.database.repositories import (
        get_skills,
        get_spells,
    )

    skills = [s for s in get_skills(test_engine) if "Lingering" in (s.SkillName or "")]
    spells = [
        s
        for s in get_spells(test_engine, obtainable_only=False)
        if "Lingering" in (s.SpellName or "")
    ]

    if not skills or not spells:
        pytest.skip("Test requires Lingering Inferno in database")

    # Create mapping rules: include skill, exclude spell
    rules = MappingRules()
    skill_ref = EntityRef.from_skill(skills[0])
    spell_ref = EntityRef.from_spell(spells[0])

    rules.excluded_keys.add(spell_ref.stable_key)
    rules.custom_mappings[skill_ref.stable_key] = "Lingering Inferno"

    # Build registry with exclusions
    builder = RegistryBuilder()
    builder.build_from_db(test_engine, test_registry, rules)

    # Verify spell is not in registry
    assert test_registry.resolve_entity(spell_ref) is None

    # Verify skill is in registry
    skill_page = test_registry.resolve_entity(skill_ref)
    assert skill_page is not None
    assert skill_page.title == "Lingering Inferno"

    # Run update
    list(ability_update_service.update_pages(test_engine))

    # Read generated page
    content = test_output_storage.read(skill_page)
    assert content is not None

    # Count Ability infoboxes
    infobox_count = content.count("{{Ability")
    assert infobox_count == 1, f"Expected 1 infobox (skill only), found {infobox_count}"

    # Verify skill ID is present
    assert str(skills[0].Id) in content

    # Verify spell ID is NOT present
    assert str(spells[0].Id) not in content


def test_all_entities_excluded_no_page(
    test_engine: Engine,
    test_registry: WikiRegistry,
    ability_update_service: UpdateService,
    test_output_storage: PageStorage,
) -> None:
    """If all entities mapping to a page are excluded, page should not exist."""
    # Create scenario where all entities for a page are excluded
    rules = MappingRules()

    # Find a spell and exclude it
    from erenshor.infrastructure.database.repositories import get_spells

    spells = list(get_spells(test_engine, obtainable_only=False))[:1]
    if not spells:
        pytest.skip("No spells in test database")

    spell = spells[0]
    spell_ref = EntityRef.from_spell(spell)
    rules.excluded_keys.add(spell_ref.stable_key)

    # Build registry with exclusion
    builder = RegistryBuilder()
    builder.build_from_db(test_engine, test_registry, rules)

    # Verify entity is not registered
    assert test_registry.resolve_entity(spell_ref) is None

    # Run update
    list(ability_update_service.update_pages(test_engine))

    # Verify page does not exist
    # Since entity is not registered, we can't use read()
    # Check if any page file exists for this spell name
    import os

    output_dir = test_output_storage.pages_dir
    matching_files = [
        f for f in os.listdir(output_dir) if spell.SpellName.replace(" ", "_") in f
    ]
    assert len(matching_files) == 0, (
        "Page should not exist when all entities are excluded"
    )


