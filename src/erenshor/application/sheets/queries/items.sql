SELECT
    ItemDBIndex,
    StableKey,
    ItemName,
    Lore,
    RequiredSlot,
    ThisWeaponType,
    (SELECT GROUP_CONCAT(ClassName, ', ')
     FROM ItemClasses ic
     WHERE ic.ItemStableKey = i.StableKey
     ORDER BY ClassName) AS Classes,
    Quality,
    ItemLevel,
    HP,
    AC,
    Mana,
    Str,
    End,
    Dex,
    Agi,
    Int,
    Wis,
    Cha,
    Res,
    MR,
    ER,
    PR,
    VR,
    StrScaling,
    EndScaling,
    DexScaling,
    AgiScaling,
    IntScaling,
    WisScaling,
    ChaScaling,
    ResistScaling,
    MitigationScaling,
    WeaponDmg,
    WeaponDly,
    Shield,
    WeaponProcChance,
    WeaponProcOnHitStableKey,
    IsWand,
    WandRange,
    WandProcChance,
    WandEffectStableKey,
    --WandBoltColorR,
    --WandBoltColorG,
    --WandBoltColorB,
    --WandBoltColorA,
    --WandBoltSpeed,
    --WandAttackSoundName,
    IsBow,
    BowEffectStableKey,
    BowProcChance,
    BowRange,
    --BowArrowSpeed,
    --BowAttackSoundName,
    ItemEffectOnClickStableKey,
    ItemSkillUseStableKey,
    TeachSpellStableKey,
    TeachSkillStableKey,
    AuraStableKey,
    WornEffectStableKey,
    SpellCastTime,
    AssignQuestOnReadStableKey,
    CompleteOnReadStableKey,
    Template,
    TemplateIngredientIds,
    TemplateRewardIds,
    ItemValue,
    SellValue,
    Stackable,
    Disposable,
    "Unique",
    Relic,
    NoTradeNoDestroy,
    BookTitle,
    Mining,
    FuelSource,
    FuelLevel,
    SimPlayersCantGet,
    AttackSoundName,
    ItemIconName,
    EquipmentToActivate,
    HideHairWhenEquipped,
    HideHeadWhenEquipped,
    ResourceName--,
    --WikiString
FROM items i
LEFT JOIN ItemStats s ON s.ItemStableKey = i.StableKey
ORDER BY i.ItemDBIndex,
    CASE s.Quality
        WHEN 'Normal' THEN 1
        WHEN 'Blessed' THEN 2
        WHEN 'Godly' THEN 3
        ELSE 4
    END;
